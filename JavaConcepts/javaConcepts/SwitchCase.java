package javaConcepts;

public class SwitchCase {

	public static void main(String[] args) {


		String  browser="firefox";
		
	switch(browser) {
	
	case "chrome": System.out.println("opening chrome");

	case "Edge" : System.out.println("OPENING EDGE");
	
	case "firefox" : System.out.println("opening firefox");
	
	default : System.out.println("please provide corrcet browser name");
	
	
	}
	 
		
		

	}

}
